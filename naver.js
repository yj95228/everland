var infowindow = new naver.maps.InfoWindow({
  content: ['<div class="iw_inner">', '<p>', '에러', '</p>', '</div>'].join(''),
});

naver.maps.Event.once(map, 'init', function () {
  map.data.setStyle(function (feature) {
    var mantle_properties =
      feature.geometryCollection[0].getRaw().mantle_properties;
    var styleOptions = {
      ...mantle_properties,
    };
    return styleOptions;
  });

  map.data.addGeoJson(zootopia, true);
  map.data.addGeoJson(globalfare, true);
  map.data.addGeoJson(american_adventure, true);
  map.data.addGeoJson(magicland, true);
  map.data.addGeoJson(european_adventure, true);
  map.data.addGeoJson(carribean, true);
  map.data.addGeoJson(poi, true);

  map.data.addListener('mouseover', function (e) {
    var feature = e.feature;
    propertyName = feature.getProperty('name');

    infowindow.setContent(
      ['<div class="iw_inner">', '<p>', propertyName, '</p>', '</div>'].join('')
    );
    infowindow.open(map, e.coord);
    map.data.overrideStyle(feature, {
      fillOpacity: 0.7,
      strokeWeight: 3,
      strokeOpacity: 0.7,
    });
  });

  map.data.addListener('mouseout', function (e) {
    if (infowindow.getMap()) {
      infowindow.close();
    }
    map.data.revertStyle();
  });
});
